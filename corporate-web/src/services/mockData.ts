// ── Documents helper ───────────────────────────────────────

function makeDocList(uploaded: boolean, statuses?: Partial<Record<string, string>>): KYCDocument[] {
  const docDefs: { type: string; label: string; required: boolean; maxMb: number; types: string[] }[] = [
    { type: "cac_certificate", label: "CAC Certificate", required: true, maxMb: 5, types: ["pdf", "jpg", "png"] },
    { type: "memart", label: "MEMART", required: true, maxMb: 10, types: ["pdf"] },
    { type: "board_resolution", label: "Board Resolution", required: true, maxMb: 5, types: ["pdf"] },
    { type: "tax_clearance", label: "Tax Clearance Certificate", required: true, maxMb: 5, types: ["pdf", "jpg", "png"] },
    { type: "scuml_certificate", label: "SCUML Certificate", required: false, maxMb: 5, types: ["pdf", "jpg", "png"] },
    { type: "proof_of_address", label: "Proof of Business Address", required: true, maxMb: 5, types: ["pdf", "jpg", "png"] },
    { type: "form_cac2_cac7", label: "Form CAC 2 / CAC 7", required: true, maxMb: 5, types: ["pdf"] },
  ];
  return docDefs.map((dd) => ({
    id: `doc-${dd.type}`,
    document_type: dd.type as KYCDocument["document_type"],
    file_name: uploaded ? `${dd.type}.pdf` : "",
    file_size: uploaded ? 1024000 : 0,
    mime_type: uploaded ? "application/pdf" : "",
    status: (statuses?.[dd.type] ?? (uploaded ? "uploaded" : "not_uploaded")) as KYCDocument["status"],
    required: dd.required,
    max_size_mb: dd.maxMb,
    allowed_types: dd.types,
    uploaded_at: uploaded ? d("2026-01-10T10:00:00Z") : null,
    rejection_comment: null,
  }));
}

