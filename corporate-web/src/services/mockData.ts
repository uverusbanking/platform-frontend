// ── Derived summaries ──────────────────────────────────────

export function getApplicationSummaries(): ApplicationSummary[] {
  return mockApplications.map((app) => ({
    id: app.id,
    company_name: app.company_info?.company_name ?? "Untitled Application",
    status: app.status,
    risk_classification: app.risk_score?.classification ?? null,
    current_step: app.current_step,
    steps_completed: app.steps_completed,
    director_verification_summary: {
      total: app.directors.length,
      verified: app.directors.filter((d) => d.verification_status === "verified").length,
      pending: app.directors.filter((d) => ["not_sent", "email_sent", "link_opened"].includes(d.verification_status)).length,
      failed: app.directors.filter((d) => ["failed", "expired"].includes(d.verification_status)).length,
    },
    feedback_count: app.feedback.filter((f) => !f.resolved).length,
    created_at: app.created_at,
    updated_at: app.updated_at,
    submitted_at: app.submitted_at,
  }));
}

export function getDashboardSummary(): DashboardSummary {
  const summaries = getApplicationSummaries();
  const allTimeline = mockApplications.flatMap((a) => a.timeline);
  allTimeline.sort((a, b) => new Date(b.created_at).getTime() - new Date(a.created_at).getTime());

  return {
    total_applications: summaries.length,
    by_status: {
      draft: summaries.filter((s) => s.status === "draft").length,
      submitted: summaries.filter((s) => s.status === "submitted").length,
      under_review: summaries.filter((s) => s.status === "under_review").length,
      approved: summaries.filter((s) => s.status === "approved").length,
      rejected: summaries.filter((s) => s.status === "rejected").length,
      returned_for_correction: summaries.filter((s) => s.status === "returned_for_correction").length,
    },
    recent_activity: allTimeline.slice(0, 10),
  };
}

export function getApplicationById(id: string): Application | undefined {
  return mockApplications.find((a) => a.id === id);
}
